def perms_to_pex():
    '''
    Clear Tables
    '''
    Permission.objects.exclude(ptype__gte=2).exclude(ptype__exact=1,permission__regex=r'^((?!name).)*$').delete()
    Entity.objects.all().delete()
    Inheritance.objects.all().delete()

    '''
    Main Permissions
    '''
    perms = SitePerm.objects.all()# Site defined permissionsexcept invividual perms and system rows
    for permi in perms:
        if permi.perm is not None and len(permi.perm) > 0:
            pex = Permission()# table ready to add new data
            pex.name = str(permi.minrank)
            pex.permission = ('-' if permi.bool == 'false' else '') + permi.perm
            pex.ptype = 0
            pex.world = permi.world if permi.world != 'All' and permi.world is not None else ''
            pex.value = ''
            pex.save()

    '''
    Users & Ranks
    '''
    users = SitePlayer.objects.all()# Site defined users
    ranks = Rank.objects.all()# Site defined ranks

    for user in users:
        pex_entity = Entity()# table ready to add new data
        pex_entity.name = str(user.uuid)
        pex_entity.etype = 1
        pex_entity.nick = user.player
        pex_entity.save()

        pex_inherit = Inheritance()# table ready to add new data
        pex_inherit.child = str(user.uuid)
        pex_inherit.parent = str(user.rank)
        pex_inherit.itype = 1
        pex_inherit.save()

        pex = Permission()# table ready to add new data
        pex.name = str(user.uuid)
        pex.ptype = 1
        pex.permission = 'name'
        pex.value = user.player
        pex.world = ''
        pex.save()        
    for rank in ranks:
        pex_entity = Entity()# table ready to add new data
        pex_entity.name = str(rank.rank)
        pex_entity.etype = 0
        pex_entity.save()

        if rank.rank != 'default':
            pex_inherit = Inheritance()# table ready to add new data
            pex_inherit.child = str(rank.rank)
            pex_inherit.parent = str(get_object_or_404(Rank,order__exact=(rank.order-1)))# Rank.objects.get(order__exact=(rank.order-1))
            pex_inherit.itype = 0
            pex_inherit.save()

        if len(rank.prefix) > 0:
            pex = Permission()# table ready to add new data
            pex.name = str(rank.rank)
            pex.ptype = 0
            pex.permission = 'prefix'
            pex.value = rank.prefix
            pex.world = ''
            pex.save()

        if len(rank.suffix) > 0:
            pex = Permission()# table ready to add new data
            pex.name = str(rank.rank)
            pex.ptype = 0
            pex.permission = 'suffix'
            pex.value = rank.suffix            
            pex.world = ''
            pex.save()

def kits_to_ess():
    kitstring = ''
    space = '  '
    newline = '\n'
    for kit,details in kits.items():
        if len(kitstring) > 0:
            kitstring += newline
        kitstring += space + kit + ':'#  protect:
        kitstring += newline + space + space + 'delay: ' + str(details[1])# delay: 86400
        kitstring += newline + space + space + 'items:'
        for detail,extra in details[2].items():
            item_qty = detail.split('-')
            kitstring += newline + space + space + space + '- ' + item_qty[0] + ' ' + item_qty[1]# - 284:0 1
            for atr,val in extra.items():
                kitstring += ' ' + atr + ':' + str(val)# efficiency:5 unbreaking:3 fortune:3 name:&4MegaPick lore:Tool
    return kitstring

def prettycolours(string):
    pref = '<span>' + string + '</span>'
    for k,v in colours.items():
        pref = pref.replace('&'+k, '</span><span style=\'color:'+ v +'\'>')
    return pref

def grids(itemid):
    data = itemdata[itemid]
    posL = itempos[itemid][0] if itemid in itempos else '0'
    posT = itempos[itemid][1] if itemid in itempos else '0'
    itype = 'itemimg'
    if itemid.startswith('373_'):
        itype='itempotion'
    name = data[0]
    states = data[1]
    grid = ''
    returngrid = ''
    gridtype = ''
    itemsmade = 1
    if len(data) > 3:
        gridlist = data[3]
        if len(gridlist) > 0:
            gridtype = gridlist[0]
            if gridtype == 'craft':
                itemsmade = gridlist[10]
                recipe = gridlist[1:10]
            else:
                recipe = gridlist[1:]
            if len(recipe) > 0:
                for bit in recipe:
                    if len(bit) > 0:
                        btype = 'itemimg'
                        if bit.startswith('373_'):
                            btype='itempotion'
                        returngrid += bit + '/' + (itempos[bit][0] + '+' + itempos[bit][1] if bit in itempos else '0+0') + '/' + btype
                    returngrid += ','
    grid = [name,gridtype,itemsmade,returngrid,posL,posT,itype]
    return grid

